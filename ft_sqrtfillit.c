int		ft_sqrtfillit(int n)
{
	int		nb;
	int		res;
	int		i;

	i = 0;
	res = 0;
	nb = 1;
	while (res < n)
	{
		res = nb * nb;
		nb++;
	}
	nb--;
	return (nb);
}
